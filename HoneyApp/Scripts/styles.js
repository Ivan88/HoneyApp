//анімація менюшки
$(window).scroll(function () {

    if ($(window).scrollTop() == 0) {
        $(".menuLogo").animate({height: "40px", width: '40px'}, 1000);
        $(".menuHolder").animate({height: "50px"}, 1000);
    }
    if ($(window).scrollTop() >= 10) {
        $('.menuLogo').css('width', '30px').css('height', '30px');
        $('.menuHolder').css('height', '40px');
    }
});
//кінець анімації менюшки
//при певній координаті зникає текст "на початок"
$(window).scroll(function () {
    if ($(window).scrollTop() < 550) {
        $('#start').fadeOut(500);
    }
    ;
    if ($(window).scrollTop() >= 550) {
        $('#start').fadeIn(500);
    }
});

//початок паралакса
$(window).scroll(function (e) {
    parallaxScroll();
});
function parallaxScroll() {
    var scrolled = $(window).scrollTop();
    $('.parallax_honey_img_1').css('top', (0 - (scrolled * .15)) + 'px');
    $('.parallax_honey_img_2').css('top', (0 - (scrolled * .5)) + 'px');


};
