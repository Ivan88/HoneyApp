$(window).ready(function () {
    $('.nav_container_hony').click(function () {
        $('.container_modal_window').fadeIn();
        //заборона прокрутки
        document.onmousewheel = document.onwheel = function () {
            return false;
        };
    });
    $('.container_modal_window,.modal_window').click(function () {
        $('.container_modal_window').fadeOut();
        //прокрутка дозволяється
        document.onmousewheel = document.onwheel = function () {
            return true;
        };
    });
});
